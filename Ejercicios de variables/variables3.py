# Asignación múltiple
dia, mes, anho = "Lunes", "Mayo", 2022
print(dia, mes, anho)

print('Hoy es', dia)
print(', el mes actual es', mes)
print('y el año es', anho)

print('Hoy es', dia, ', el mes actual es', mes, 'y el año es', anho)

# 'Hoy es Martes, el mes actual es Diciembre y el año es 2021'

